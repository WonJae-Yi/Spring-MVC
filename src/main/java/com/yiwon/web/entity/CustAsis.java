package com.yiwon.web.entity;

public class CustAsis {
	
	public String jumin;
	public String custNo;
	
	public CustAsis() {
		
	}

	public CustAsis(String jumin, String custNo) {
		this.jumin = jumin;
		this.custNo = custNo;
	}

	public String getJumin() {
		return jumin;
	}

	public void setJumin(String jumin) {
		this.jumin = jumin;
	}

	public String getCustNo() {
		return custNo;
	}

	public void setCustNo(String custNo) {
		this.custNo = custNo;
	}
	
	
	

}
